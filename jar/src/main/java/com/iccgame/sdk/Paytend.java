    /***
     * 判断是否已安装聚财通安全支付apk
     *
     * @param context
     * @param packageName
     * @return
     */
    public PackageInfo isAvilible(Context context, String packageName) {
        final PackageManager packageManager = context.getPackageManager();// 获取 package manager
        List<PackageInfo> packages = packageManager.getInstalledPackages(0);// 获取所有已安装程序的包信息
        for (PackageInfo item : packages) {
            if (item.packageName.equals(packageName) == false) {
                continue;
            }
            return item;
        }
        return null;
    }

